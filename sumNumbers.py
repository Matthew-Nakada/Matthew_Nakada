num1 = input("Pick a number: ")
num2 = input("Pick another number: ")
sum = float(num1) + float(num2)
print("The sum of {0} and {1} is {2}".format(num1, num2, sum))

num3 = input("Pick yet another number: ")
num4 = input("Do not NOT pick a number: ")
diff = float(num4) - float(num3)
print("The difference between {0} and {1} is {2}".format(num4, num3, diff))